package bbva.delivery.tarjetas.bean;

public class Configuracion {

	private Integer idConfiguracio;
	private String parametro;
	private String valor;
	private String descripcion;

	public Integer getIdConfiguracio() {
		return idConfiguracio;
	}

	public void setIdConfiguracio(Integer idConfiguracio) {
		this.idConfiguracio = idConfiguracio;
	}

	public String getParametro() {
		return parametro;
	}

	public void setParametro(String parametro) {
		this.parametro = parametro;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
}
